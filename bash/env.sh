# TMP and TEMP are defined in the Windows environment.  Leaving
# them set to the default Windows temporary directory can have
# unexpected consequences.
unset TMP
unset TEMP

# Use emacs as the default text editor
export EDITOR='emacs'

# Add the bin folder in the home folder as the first entry in the path. This
# means that the home folder is searched first of all folders in the path to
# find a given command.
export PATH=~/configuration/bin:$PATH

